    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        try {
            int saldo = credito.saldo(this.cliente.nit);
            int pago  = Integer.parseInt(jTextField6.getText());
            int valor = this.cliente.credito - pago + saldo;
            if( (valor) < 0){
                JOptionPane.showMessageDialog(rootPane, "No tiene suficiente crédito");
            } else {
                this.recalcularCaja();
            }
        } catch (Exception ex) {
            Logger.getLogger(Cajero.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

