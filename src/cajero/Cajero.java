    private void recalcularCaja(){
        int total = 0;
        total += Integer.parseInt(jTextField4.getText());
        total += Integer.parseInt(jTextField5.getText());
        total += Integer.parseInt(jTextField6.getText());
        total += Integer.parseInt(jTextField7.getText());
        jTextField8.setText(Integer.toString(this.monto-total));
        if (total == this.monto) {
            jButton11.setEnabled(true);
        } else {
            jButton11.setEnabled(false);
        }
    }

